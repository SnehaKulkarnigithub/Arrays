class SwitchExample
{
	public static void main(String[] args)
	{
		int a=1*20;
		 switch(a)
		{
			 case 1:
				 System.out.println("Inside case1");
			 break;
			 case 2:
				 System.out.println("Inside case2");
			 break;
			 case 3:
				 System.out.println("Inside case3");
			 break;
			 case 1*20:
				 System.out.println("Inside case 1*20");
			 break;
			 default:
				 System.out.println("Inside Default");
		}
	}
}