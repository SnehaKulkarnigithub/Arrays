class HWStaticVariable
{
	static byte a;
	static short b;
	static int c;
	static long d;
	static float e;
	static double f;
	static char g;
	static boolean h;
	
	public static void main(String[] args)
	{
		System.out.println("The default value for byte is: "+a);
		System.out.println("The default value for short is: "+b);
		System.out.println("The default value for int is: "+c);
		System.out.println("The default value for long is: "+d);
		System.out.println("The default value for float is: "+e);
		System.out.println("The default value for double is: "+f);
		System.out.println("The default value for char is: "+g);
		System.out.println("The default value for boolean is: "+h);
	}
	
}