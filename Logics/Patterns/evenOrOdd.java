
public class evenOrOdd {

	public static void main(String[] args) {
		for(int i=1; i<=50; i++)
		{
			if((i % 2)==0)
			{
				System.out.println("The Number "+i+" is Even");
			}
			else
				System.out.println("The Number "+i+" is Odd");
			
		}
	}

}

